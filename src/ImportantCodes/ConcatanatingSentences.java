package ImportantCodes;

public class ConcatanatingSentences {
    public static void main(String[] args) {
        String Firstname="Ayesha";
        String Lastname="Saif";
        String city="Toronto";
        String state="Ontario";
        String number="123-456-7891";

        System.out.println("My first name is "+Firstname+" and my last name is "+Lastname);
        System.out.println("I live in "+city+" and state "+state);
        System.out.println("My phone number is "+number);

        Firstname= "Abi";
        Lastname= "Alghas";
        city="Vancouver";
        state="British Columbia";
        number= "321-546-789";

        System.out.println("My first name is "+Firstname+" and my last name is "+Lastname);
        System.out.println("I live in "+city+" and state "+state);
        System.out.println("My phone number is "+number);















    }}
