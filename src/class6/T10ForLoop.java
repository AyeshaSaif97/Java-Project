package class6;

public class T10ForLoop {
    public static void main(String[] args) {

//print only odd numbers from 100 to 1


        for (int i = 99; i>=1 ; i-=2) {
            System.out.print(i+" ");

        }
        System.out.println();



        int i=99;
        while(i>=1){
            System.out.print(i+" ");
            i-=2;
        }
    }
}
