package ReplitsPractice;

public class Number45 {
    public static void main(String[] args) {
        /*
        **For you to do:** while loop
Using while loop print numbers from 0 to 8
**Output:** 012345678
         */

        int i = 0;

        while (i <= 8) {
            System.out.println(i);
            i++;
        }
    }
}
