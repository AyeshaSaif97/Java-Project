package class7;

public class E7NestedLoops {
    public static void main(String[] args) {
/*
Right Triangle of Numbers Write a program to print a right triangle of numbers using nested loops:
 */

        for (int i = 0; i < 5; i++) {
            for (int j = i; j < 5; j++) {
                System.out.print("*");
            }
            System.out.println();
        }















    }
}
