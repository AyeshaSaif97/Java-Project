package review3;

public class E6WhileLoop {
    public static void main(String[] args) {
        /*
        while loop is good (usually we go with a variable)   condition is checked first
        do while loop is good when you are taking an input from the user .... code is printed first then the condition is checked
        for loop is good when you know exactly how many times you want to print it


         */

        int x = 1;
        while (x<=10) {
            System.out.println(x);
            x++;
        }
    }
}
